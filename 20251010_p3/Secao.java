public class Secao {
    private int idSecao;
    private String nome;


    public Secao(int idSecao, String nome) {
        this.idSecao = idSecao;
        this.nome = nome;
    }


    public int getIdSecao() {
        return idSecao;
    }


    public void setIdSecao(int idSecao) {
        this.idSecao = idSecao;
    }


    public String getNome() {
        return nome;
    }


    public void setNome(String nome) {
        this.nome = nome;
    } 


}
